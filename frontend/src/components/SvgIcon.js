// V228 — Liste des noms disponibles, exportee pour permettre a un lot suivant
// de verifier qu'un nom existe avant de l'utiliser (ou de generer une galerie).
export const SVG_ICON_NAMES = Object.keys(V228_PATHS);

/**
 * V228 — Icone SVG inline.
 *
 * @param {string} name    Nom de l'icone (voir SVG_ICON_NAMES).
 * @param {number} size    Cote en px (largeur = hauteur). Defaut 16.
 * @param {string} color   Couleur du trait. Defaut `currentColor`, ce qui fait
 *                         suivre la couleur du texte environnant.
 * @param {string} className
 * @param {object} style   Fusionne PAR-DESSUS le style de base, pour pouvoir
 *                         surcharger `verticalAlign` si un appelant le demande.
 *
 * Retourne `null` sur un nom inconnu : un emoji manquant ne doit jamais faire
 * tomber une page de production. Aucune exception n'est levee.
 */
export default function SvgIcon({ name, size = 16, color, className = '', style = {}, ...props }) {
  const glyph = V228_PATHS[name];
  if (!glyph) return null;

  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill="none"
      stroke={color || 'currentColor'}
      strokeWidth="2"
      strokeLinecap="round"
      strokeLinejoin="round"
      className={className}
      style={{ display: 'inline-block', verticalAlign: 'middle', flexShrink: 0, ...style }}
      // V228: l'icone est decorative par defaut — le libelle textuel qu'elle
      // accompagne porte deja le sens. Un appelant peut surcharger via props
      // (role="img" + aria-label) puisque {...props} est applique APRES.
      aria-hidden="true"
      focusable="false"
      {...props}
    >
      {glyph}
    </svg>
  );
}
